// Using Variables, Literals, and Constants
public class ex02 {
	
	public static void main(String[] args) {
		final double PI = 3.14 ; // Declare pi as a constant
		
		double radius = 10.0 ;
		double CircleArea = radius * radius * PI ;
		
		System.out.println("Circle area = " + CircleArea) ;
	}
}
